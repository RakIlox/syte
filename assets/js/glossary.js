class Glossary {
    constructor() {
        this.glossaryData = glossaryData;
        this.searchInput = null;
        this.gridContainer = null;
        this.modal = null;
        this.currentTerm = null;
    }

    init() {
        this.searchInput = document.getElementById('search-input');
        this.gridContainer = document.querySelector('.glossary-grid');
        this.modal = document.getElementById('term-modal');
        
        if (!this.gridContainer) return;
        
        this.renderGlossary(this.glossaryData);
        this.bindEvents();
        this.initModal();
    }

    renderGlossary(items) {
        if (!this.gridContainer) return;
        
        this.gridContainer.innerHTML = items.map(item => `
            <div class="term-card" data-term-id="${item.id}">
                <div class="term-icon">
                    <i class="fas fa-${item.icon}"></i>
                </div>
                <div class="term-content">
                    <h3>${item.term}</h3>
                    <span class="term-category">${item.category}</span>
                    <p>${item.definition}</p>
                    <div class="term-example">
                        <i class="fas fa-lightbulb"></i>
                        <span>${item.example}</span>
                    </div>
                </div>
                <div class="term-arrow">
                    <i class="fas fa-chevron-right"></i>
                </div>
            </div>
        `).join('');
        
        this.updateStats();
    }

    updateStats() {
        const totalTerms = this.glossaryData.length;
        const attackTerms = this.glossaryData.filter(t => t.category === 'Атаки').length;
        const protectionTerms = this.glossaryData.filter(t => t.category === 'Защита').length;
        
        this.animateNumber('total-terms', totalTerms);
        this.animateNumber('attack-terms', attackTerms);
        this.animateNumber('protection-terms', protectionTerms);
    }
    
    animateNumber(id, target) {
        const el = document.querySelector(`[data-count="${id}"]`);
        if (!el) return;
        
        const current = parseInt(el.textContent) || 0;
        if (current === target) return;
        
        const increment = target > current ? 1 : -1;
        const duration = 1000;
        const steps = Math.abs(target - current);
        const stepTime = duration / steps;
        
        let value = current;
        const timer = setInterval(() => {
            value += increment;
            el.textContent = value;
            if (value === target) clearInterval(timer);
        }, Math.max(stepTime, 50));
    }

    bindEvents() {
        if (this.searchInput) {
            this.searchInput.addEventListener('input', (e) => {
                this.filterGlossary(e.target.value.toLowerCase());
            });
        }

        document.querySelectorAll('.category-btn').forEach(btn => {
            btn.addEventListener('click', (e) => {
                const button = e.target.closest('.category-btn');
                if (!button) return;
                
                document.querySelectorAll('.category-btn').forEach(b => b.classList.remove('active'));
                button.classList.add('active');
                this.filterByCategory(button.dataset.category);
            });
        });

        if (this.gridContainer) {
            this.gridContainer.addEventListener('click', (e) => {
                const card = e.target.closest('.term-card');
                if (card) {
                    const termId = card.dataset.termId;
                    this.showTermDetails(termId);
                }
            });
        }
    }

    filterByCategory(category) {
        // Обновляем визуальное состояние всех кнопок категорий
        document.querySelectorAll('.category-btn').forEach(btn => {
            btn.classList.toggle('active', btn.dataset.category === category);
        });
        
        if (category === 'all') {
            document.querySelectorAll('.term-card').forEach(card => {
                card.style.display = '';
            });
            return;
        }

        const cards = document.querySelectorAll('.term-card');
        cards.forEach(card => {
            const termId = card.dataset.termId;
            const term = this.glossaryData.find(t => t.id === termId);
            if (term && term.category === category) {
                card.style.display = '';
            } else {
                card.style.display = 'none';
            }
        });
    }

    initModal() {
        if (!this.modal) return;

        // Обработчики для ВСЕХ кнопок .modal-close (× и "Закрыть")
        this.modal.querySelectorAll('.modal-close').forEach(btn => {
            btn.addEventListener('click', (e) => {
                e.preventDefault();
                e.stopPropagation();
                this.closeModal();
            });
        });

        // Закрытие по клику на фон модалки
        this.modal.addEventListener('click', (e) => {
            if (e.target === this.modal) {
                this.closeModal();
            }
        });

        // Закрытие по Escape
        document.addEventListener('keydown', (e) => {
            if (e.key === 'Escape' && this.modal.classList.contains('active')) {
                e.preventDefault();
                this.closeModal();
            }
        });
    }

    showTermDetails(termId) {
        const term = this.glossaryData.find(t => t.id === termId);
        if (!term) return;

        this.currentTerm = term;

        const modalTitle = this.modal.querySelector('#modal-term-title');
        const modalIcon = this.modal.querySelector('#modal-term-icon');
        const modalCategory = this.modal.querySelector('#modal-term-category');
        const modalDefinition = this.modal.querySelector('#modal-term-definition');
        const modalExample = this.modal.querySelector('#modal-term-example');
        const modalFullArticle = this.modal.querySelector('#modal-full-article');

        if (modalTitle) modalTitle.textContent = term.term;
        if (modalIcon) modalIcon.innerHTML = `<i class="fas fa-${term.icon}"></i>`;
        if (modalCategory) modalCategory.textContent = term.category;
        if (modalDefinition) modalDefinition.textContent = term.definition;
        if (modalExample) modalExample.innerHTML = `<i class="fas fa-lightbulb"></i> ${term.example}`;
        
        if (modalFullArticle) {
            modalFullArticle.innerHTML = this.formatArticle(term.fullArticle || term.definition);
        }

        this.modal.classList.add('active');
        document.body.style.overflow = 'hidden';
    }

    formatArticle(text) {
        if (!text) return '';
        
        let html = text.replace(/</g, '<').replace(/>/g, '>');
        html = html.replace(/\*\*(.+?)\*\*/g, '<strong>$1</strong>');
        html = html.replace(/(\d+\.\s)([^\n]+)/g, '<div class="article-list-item"><span class="list-number">$1</span>$2</div>');
        html = html.replace(/(-\s)([^\n]+)/g, '<div class="article-list-item"><span class="list-bullet">•</span>$2</div>');
        html = html.replace(/\n\n/g, '</p><p>');
        html = html.replace(/\n/g, '<br>');
        
        return `<p>${html}</p>`;
    }

    closeModal() {
        if (this.modal && this.modal.classList.contains('active')) {
            this.modal.classList.remove('active');
            document.body.style.overflow = '';
            this.currentTerm = null;
        }
    }

    filterGlossary(searchTerm) {
        const cards = document.querySelectorAll('.term-card');
        const terms = this.glossaryData;
        
        cards.forEach(card => {
            const termId = card.dataset.termId;
            const term = terms.find(t => t.id === termId);
            
            if (term) {
                const matchesSearch = 
                    term.term.toLowerCase().includes(searchTerm) ||
                    term.definition.toLowerCase().includes(searchTerm) ||
                    term.category.toLowerCase().includes(searchTerm);
                
                card.style.display = matchesSearch ? '' : 'none';
            }
        });
    }
}

// Инициализация при загрузке страницы
document.addEventListener('DOMContentLoaded', () => {
    new Glossary().init();
});